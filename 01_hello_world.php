<?php
/**
 * 01_hello_world.php - 最初のPHPプログラム
 *
 * PHPの基本的な構文と出力方法を学びます。
 */

// ============================================
// PHPの基本構造
// ============================================
// PHPコードは <?php で始まります
// コメントは // で1行、/* */ で複数行書けます

// ============================================
// echo - 文字列を出力する
// ============================================
// echoは最も基本的な出力方法です

echo "Hello, World!";
// 出力: Hello, World!

echo "\n"; // \n は改行を意味します

// ============================================
// 複数の値を出力する
// ============================================
// echoはカンマで区切って複数の値を出力できます

echo "こんにちは、", "PHP", "の世界へ！", "\n";
// 出力: こんにちは、PHPの世界へ！

// ============================================
// print - もう一つの出力方法
// ============================================
// printはechoと似ていますが、1つの値しか出力できません
// printは戻り値として1を返します

print "printでも出力できます\n";
// 出力: printでも出力できます

// ============================================
// シングルクォートとダブルクォート
// ============================================
// ダブルクォート: 変数や特殊文字（\n等）が展開される
// シングルクォート: そのまま文字列として扱われる

echo "ダブルクォート: 改行は\nここで起きます\n";
// 出力: ダブルクォート: 改行は
//       ここで起きます

echo 'シングルクォート: 改行は\nそのまま表示されます', "\n";
// 出力: シングルクォート: 改行は\nそのまま表示されます

// ============================================
// 実行方法
// ============================================
// ターミナルで以下のコマンドを実行してください：
// php 01_hello_world.php
