<?php
/**
 * 02_variables.php - 変数とデータ型
 *
 * PHPの変数の宣言方法と基本的なデータ型を学びます。
 */

// ============================================
// 変数の基本
// ============================================
// PHPの変数は $ で始まります
// 変数名は文字またはアンダースコアで始める必要があります

$message = "こんにちは";
echo $message, "\n";
// 出力: こんにちは

// ============================================
// データ型1: 文字列 (string)
// ============================================
$name = "田中太郎";
$greeting = "私の名前は{$name}です"; // ダブルクォート内で変数を展開

echo $greeting, "\n";
// 出力: 私の名前は田中太郎です

// ============================================
// データ型2: 整数 (integer)
// ============================================
$age = 25;
$year = 2024;

echo "年齢: ", $age, "歳\n";
// 出力: 年齢: 25歳

echo "年: ", $year, "年\n";
// 出力: 年: 2024年

// ============================================
// データ型3: 浮動小数点数 (float)
// ============================================
$price = 1980.50;
$pi = 3.14159;

echo "価格: ", $price, "円\n";
// 出力: 価格: 1980.5円

echo "円周率: ", $pi, "\n";
// 出力: 円周率: 3.14159

// ============================================
// データ型4: 真偽値 (boolean)
// ============================================
$is_student = true;
$is_teacher = false;

echo "学生ですか: ", ($is_student ? "はい" : "いいえ"), "\n";
// 出力: 学生ですか: はい

echo "教師ですか: ", ($is_teacher ? "はい" : "いいえ"), "\n";
// 出力: 教師ですか: いいえ

// ============================================
// データ型5: NULL
// ============================================
// NULLは「値がない」ことを表します
$nothing = null;

echo "NULLの値: ";
var_dump($nothing); // var_dumpは変数の詳細を表示します
// 出力: NULLの値: NULL

// ============================================
// 型の確認
// ============================================
// gettype()で変数の型を確認できます

echo "\n--- 型の確認 ---\n";
echo "文字列の型: ", gettype($name), "\n";
// 出力: 文字列の型: string

echo "整数の型: ", gettype($age), "\n";
// 出力: 整数の型: integer

echo "小数の型: ", gettype($price), "\n";
// 出力: 小数の型: double

echo "真偽値の型: ", gettype($is_student), "\n";
// 出力: 真偽値の型: boolean

echo "NULLの型: ", gettype($nothing), "\n";
// 出力: NULLの型: NULL

// ============================================
// 変数の結合
// ============================================
$first_name = "太郎";
$last_name = "山田";

// ドット(.)で文字列を結合できます
$full_name = $last_name . " " . $first_name;
echo "フルネーム: ", $full_name, "\n";
// 出力: フルネーム: 山田 太郎

// .= で既存の変数に追加できます
$text = "Hello";
$text .= " World";
echo $text, "\n";
// 出力: Hello World
